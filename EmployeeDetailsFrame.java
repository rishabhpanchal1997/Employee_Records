    private void SAVEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SAVEActionPerformed
        // TODO add your handling code here:
        try{
            String empid,fname,lname,contact,city,salary,team;
            empid=EmpID.getText();
            fname=Fname.getText();
            lname=Lname.getText();
            contact=Contact.getText();
            city=City.getText();
            salary=Salary.getText();
            team=Team.getText();
            
            //this 'prepareStatement' is used for communication between GUI and DATABASE
            pre=con.prepareStatement("insert into employee_records(empid,fname,lname,contact,salary,city,team)values(?,?,?,?,?,?,?)");
            
             pre.setString(1, empid);
            pre.setString(2, fname);
            pre.setString(3,lname);
            pre.setString(4, contact);
            pre.setString(5,salary);
            pre.setString(6, city);
            pre.setString(7, team);
            
            pre.executeUpdate();
            JOptionPane.showMessageDialog(this,"Details Saved"); //show this meassage after saving details
            
            //make textarea empty after saving data into database
            EmpID.setText("");
            Fname.setText("");
            Lname.setText("");
            Contact.setText("");
            Salary.setText("");
            City.setText("");
            Team.setText("");
            
            EmpID.requestFocus();//cursor will automatically go back to firstname
            update_table();
        } catch (SQLException ex) {
            Logger.getLogger(EmployeeDetailsFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
    }//GEN-LAST:event_SAVEActionPerformed


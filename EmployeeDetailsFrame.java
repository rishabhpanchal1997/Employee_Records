    private void update_table() {
        int cc; //to store the number of column counted
        try{
            pre=con.prepareStatement("select * from employee_records");  //statement for execution of query
            
            ResultSet rs = pre.executeQuery(); //retreaving data from table by executing thequery
            ResultSetMetaData RSMD = rs.getMetaData(); //retrieved data stored in this variable
            cc=RSMD.getColumnCount(); //counting the number of column of the table and storing in 'cc'
            DefaultTableModel DFT = (DefaultTableModel) jTable1.getModel(); 
            DFT.setRowCount(0);
            
            //while the resultset is not null
            while(rs.next()){
                Vector v2 = new Vector(); //analogically --> it is like array which can store any type of data
                for(int i=0; i<=cc; i++){
                    v2.add(rs.getString("EmpID"));
                    v2.add(rs.getString("Fname"));
                    v2.add(rs.getString("Lname"));
                    v2.add(rs.getString("Contact"));
                    v2.add(rs.getString("Salary"));
                    v2.add(rs.getString("City"));
                    v2.add(rs.getString("Team"));
                }
                DFT.addRow(v2);//adding row in the defaulttable
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(EmployeeDetailsFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

